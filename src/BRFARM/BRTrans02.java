    private void ResetModel() {

        while (mTransection.getRowCount() > 0) {
            for (int i = 0; i < mTransection.getRowCount(); ++i) {
                mTransection.removeRow(i);
            }
        }

    }

    private void InforDetail() {

        List<String> getListMInforDetail = cld.GetResultMInforDetail(BRLogin.vFarm, BRTrans01.vTRAN_HOUSE, BRTrans01.vTRAN_DATE, BRTrans01.vTRAN_ITEM, BRTrans01.vTRAN_QTY,BRLogin.vDuckBreed);
        ResetModel();

        for (int i = 0; i < getListMInforDetail.size(); i++) {
            String[] CheckMInforDetail = getListMInforDetail.get(i).split(";");
            mTransection.insertRow(mTransection.getRowCount(), new Object[]{
                CheckMInforDetail[0].trim(),
                CheckMInforDetail[1].trim(),
                CheckMInforDetail[2].trim()
            });
        }

    }

