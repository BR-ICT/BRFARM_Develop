    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:

        if ("Yes".equals(BRUser01.OnChange)) {
            jTextUsername.setText(BRUser01.vUSE_USER);
            jTextPassword.setText(crd.CheckUserPassword(BRLogin.vFarm, BRUser01.vUSE_USER));
            jTextName.setText(BRUser01.vUSE_NAME);
            vStatus = BRUser01.vUSE_APPR;
            if ("Yes".equals(vStatus)) {
                jcbYes.setSelected(true);
            } else {
                jcbNo.setSelected(true);
            }

            if (jcbYes.isSelected() == true) {
                vStatus = "1";
            } else {
                vStatus = "0";
            }

            jTextPassword.requestFocusInWindow();
            jTextUsername.setEnabled(false);
            jButtonCreate.setVisible(false);
            jButtonUpdate.setVisible(true);
            BRUser01.OnChange = "No";
        } else {
            jTextUsername.requestFocusInWindow();
            jTextUsername.setEnabled(true);
            jButtonCreate.setVisible(true);
            jButtonUpdate.setVisible(false);
            BRUser01.OnCreate = "No";
        }

    }//GEN-LAST:event_formWindowOpened

