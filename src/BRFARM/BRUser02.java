    private void jButtonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateActionPerformed
        // TODO add your handling code here: 

        try {

            if (jTextUsername.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Insert User Name");
                jTextUsername.requestFocusInWindow();
            } else if (jTextPassword.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Insert Password");
                jTextUsername.requestFocusInWindow();
            } else if (jcbYes.isSelected() == false && jcbNo.isSelected() == false) {
                JOptionPane.showMessageDialog(null, "Select Approve");
                jcbYes.requestFocusInWindow();
            } else {

                String count = crd.CheckUserDuplicate(BRLogin.vFarm, jTextUsername.getText());

                if (Integer.valueOf(count) > 0) {
                    JOptionPane.showMessageDialog(null, jTextUsername.getText() + " Data Already");
                } else {
                    cid.InsertUser(BRLogin.vFarm, jTextUsername.getText().toUpperCase(), jTextPassword.getText(), jTextName.getText(), vStatus, "USER");
                    JOptionPane.showMessageDialog(null, "Insert Success");
                    BRUser01.FrameOpen = "No";
                    this.setVisible(false);
                }
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

    }//GEN-LAST:event_jButtonCreateActionPerformed

