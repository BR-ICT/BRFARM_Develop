    private void Login() {

        if (txtUsername.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Insert Username");
            txtUsername.requestFocusInWindow();
        } else if (txtPassword.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Insert Password");
            txtPassword.requestFocusInWindow();
        } else if ("Please Select : Farm".equals(jcbFarm.getSelectedItem().toString())) {
            JOptionPane.showMessageDialog(null, "Please Select Farm");
            jcbFarm.requestFocusInWindow();
        } else if ("Please Select : Breed".equals(jcbBreed.getSelectedItem().toString())) {
            JOptionPane.showMessageDialog(null, "Please Select Breed");
            jcbBreed.requestFocusInWindow();
        } else {

            try {

                vUsername = txtUsername.getText().trim();
                vPassword = txtPassword.getText().trim();
                String[] getFarm = jcbFarm.getSelectedItem().toString().split(":");
                vFarm = getFarm[0].trim();
                vFarmDesc = getFarm[1].trim();
                String[] getDuckType = jcbBreed.getSelectedItem().toString().split(":");
                vDuckBreed = getDuckType[0].trim();
                vDuckBreedDesc = getDuckType[1].trim();
                vDuckBreedAll = "AL";

                if ("GP".equals(vDuckBreed)) {
                    vNotDuckBreed = "PS";
                    vDuckHC = "GP";
                } else {
                    vNotDuckBreed = "GP";
                    vDuckHC = "HC";
                }

                String vGetConnectAuth = ConnectSQLite.LoginSQLite(vFarm.toUpperCase(), vUsername.toUpperCase(), vPassword.toUpperCase());

                if (!"".equals(vGetConnectAuth)) {
                    vAuth = vGetConnectAuth;
                    this.setVisible(false);
                    new BRMain().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "User or Password Incorrect");
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.toString());

            }
        }

    }
