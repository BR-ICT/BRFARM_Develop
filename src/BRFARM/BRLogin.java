    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        try {
            // Specify the path to your batch file
            String PathFile = System.getProperty("user.dir").toString();
            String batchFilePath = PathFile + "\\Install.bat\\";

            // Use the Runtime class to execute the batch file
            Process process = Runtime.getRuntime().exec("cmd /c start " + batchFilePath);

            // Wait for the process to complete (optional)
            int exitCode = process.waitFor();

            // Print the exit code (0 typically means success)
            System.out.println("Batch file execution completed with exit code: " + exitCode);
            this.setVisible(false);
            dispose();
        } catch (IOException | InterruptedException e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnupdateActionPerformed

