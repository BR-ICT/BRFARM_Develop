    private void UpdateEggTransfer() {

        String[] getEggTran = jcbEggTran.getSelectedItem().toString().split(":");
        Date getDate = null;
        String getLotDate = "";

        try {
            getDate = new SimpleDateFormat("dd/MM/yyyy", Locale.US).parse(getEggTran[0]);
        } catch (ParseException ex) {
            Logger.getLogger(BRTrans01.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (getEggTran.length >= 2) {
            getLotDate = getEggTran[1].trim();
        }

        String vCountMDesc = crd.CheckMDescDuplicate(BRLogin.vFarm, sdf.format(getDate), getLotDate, BRLogin.vDuckBreed);

        if (Integer.valueOf(vCountMDesc) > 0) {
            JOptionPane.showMessageDialog(null, "Egg Description Already");
        } else {
            cid.InsertEggDescription(BRLogin.vFarm, sdf.format(getDate), jTextHCCBW.getText(),  jTextHCCAW.getText(), jTextHCD.getText(), jTextHCS.getText(), jTextHCDT.getText(),
                     jTextHCTTL.getText(), Place, jTextTempR.getText(),  jTextTempWT.getText(), jTextTimeW.getText(), getLotDate,  jTextPHWT.getText(),  jTextCONC.getText(),
                    jTextTempCS.getText(), jTextTruckB.getText(), jTextTruckBHC.getText(), Weather, jcbGiven.getSelectedItem().toString(), jcbReceive.getSelectedItem().toString(), jcbApprove.getSelectedItem().toString(),
                    stf.format(jsTimeIn.getValue()), jTextTruckNo.getText(), stf.format(jsTimeOut.getValue()), jTextRemark1.getText(), jTextRemark2.getText(), jTextCert.getText(), BRLogin.vDuckBreed);
            StsUpdateEggTransfer = "Yes";
        }

    }

