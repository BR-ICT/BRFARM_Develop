    private void jMenuDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenuDeleteMouseClicked
        // TODO add your handling code here:
        if ("History".equals(JPanel) && jMenuDelete.isEnabled() == true) {

            int index = jTableTransection.getSelectedRow();
            if (index < 0) {
                JOptionPane.showMessageDialog(null, "Please select " + JPanel + " record for Delete!");
            } else {

                String DESC_DATE = jTableTransection.getValueAt(index, 0).toString();
                String DESC_TRUCKNO = jTableTransection.getValueAt(index, 1).toString();

                Object[] options = {"Yes", "No"};
                int n = JOptionPane.showOptionDialog(null, "Do you want to Delete data?",
                        "Confirm to Delete?",
                        JOptionPane.YES_NO_CANCEL_OPTION,
                        JOptionPane.QUESTION_MESSAGE, null, options,
                        options[1]);
                if (n == 0) // Confirm Delete = Yes
                {
                    cid.DeleteEggDesc(BRLogin.vFarm, DESC_DATE, DESC_TRUCKNO, BRLogin.vDuckBreed); // Delete Data
                    JOptionPane.showMessageDialog(null, "Delete Success");
                    EggDescription(); //Reload Table
                }
            }
        }
    }//GEN-LAST:event_jMenuDeleteMouseClicked

