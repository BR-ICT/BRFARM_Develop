    private void jButtonSendMOFMF1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSendMOFMF1ActionPerformed
        // TODO add your handling code here:

        try {

            if ("".equals(jTextMOMLF.getText())) {
                JOptionPane.showMessageDialog(null, "Please Insert MO");
            } else if ("Please Select : House".equals(jcbHouseMLF.getSelectedItem().toString())) {
                JOptionPane.showMessageDialog(null, "Please Select House");
            } else {

                String[] getFarm = jcbFarm.getSelectedItem().toString().split(":");
                String vFarm = getFarm[0].trim();
                String[] getHouse = jcbHouseMLF.getSelectedItem().toString().split(":");
                String vHouse = getHouse[0].trim();

                cid.UpdateMOGrower(vFarm, vHouse, jTextMOMLF.getText());
                JOptionPane.showMessageDialog(null, "Sending MO-Grower Duck is Completed");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

    }//GEN-LAST:event_jButtonSendMOFMF1ActionPerformed

