    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try {
            if ("Yes".equals(BRData01.OnChange)) {
                jTextCode.setText(BRData01.vFLOC_CODE);
                jXDateStart.setDate(sdf.parse(BRData01.vFLOC_STDATE));

                if ("".equals(BRData01.vFLOC_ENDATE)) {
                    jXDateEnd.setFormats(sdf);
                } else {
                    jXDateEnd.setDate(sdf.parse(BRData01.vFLOC_ENDATE));
                }

                jTextDesc.setText(BRData01.vFLOC_BREED);

                vStatus = BRData01.vFLOC_STATUS;
                if ("Yes".equals(vStatus)) {
                    jcbYes.setSelected(true);
                } else {
                    jcbNo.setSelected(true);
                }

                if (jcbYes.isSelected() == true) {
                    vStatus = "1";
                } else {
                    vStatus = "0";
                }

                jXDateWeek.setDate(sdf.parse(BRData01.vFLOC_STWEEK));
                jXDateStart.requestFocusInWindow();
                jTextCode.setEnabled(false);
                jButtonCreate.setVisible(false);
                jButtonUpdate.setVisible(true);
                BRData01.OnChange = "No";
            } else {
                jTextCode.requestFocusInWindow();
                jTextCode.setEnabled(true);
                jButtonCreate.setVisible(true);
                jButtonUpdate.setVisible(false);
                BRData01.OnCreate = "No";
            }

        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }//GEN-LAST:event_formWindowOpened

