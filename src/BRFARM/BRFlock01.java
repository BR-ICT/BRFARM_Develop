    private void jButtonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateActionPerformed
        // TODO add your handling code here: 

        try {

            if (jTextCode.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Insert Flock Code");
                jTextCode.requestFocusInWindow();
            } else if (jcbYes.isSelected() == false && jcbNo.isSelected() == false) {
                JOptionPane.showMessageDialog(null, "Select Status");
                jcbYes.requestFocusInWindow();
            } else {
                String DateStart, DateEnd, DateWeek;

                if (jXDateStart.getDate() == null) {
                    DateStart = "";
                } else {
                    DateStart = sdf.format(jXDateStart.getDate());
                }

                if (jXDateEnd.getDate() == null) {
                    DateEnd = "";
                } else {
                    DateEnd = sdf.format(jXDateEnd.getDate());
                }

                if (jXDateWeek.getDate() == null) {
                    DateWeek = "";
                } else {
                    DateWeek = sdf.format(jXDateWeek.getDate());
                }

                String count = crd.CheckFlockDuplicate(BRLogin.vFarm, jTextCode.getText());

                if (Integer.valueOf(count) > 0) {
                    JOptionPane.showMessageDialog(null, jTextCode.getText() + " Data Already");
                } else {
                    cid.InsertFlock(jTextCode.getText(), BRLogin.vFarm, DateStart, DateEnd, jTextDesc.getText(), vStatus, DateWeek, BRLogin.vDuckBreed);
                    JOptionPane.showMessageDialog(null, "Insert Success");
                    BRData01.FrameOpen = "No";
                    this.setVisible(false);
                }
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

    }//GEN-LAST:event_jButtonCreateActionPerformed

